/**
 * Calculates precise mileage and fuel metrics based on bike and riding style.
 */
export function computeBikeSpecs(bikeName, style, isHighway = true) {
  const defaultSpec = { cityMileage: 28, highwayMileage: 35, fuelPreference: "normal" };
  const customList = getCustomBikes();
  const bike = BIKES_DATABASE.find(b => b.name === bikeName) || 
               customList.find(b => b.name === bikeName) || 
               defaultSpec;

  let baseMileage = isHighway ? bike.highwayMileage : bike.cityMileage;

  // Apply riding style modifiers
  if (style === "Aggressive (Curvy passes)") {
    baseMileage *= 0.85; // lower fuel efficiency for revving
  } else if (style === "Off-road / Exploration") {
    baseMileage *= 0.80; // lower efficiency for torque/gears
  } else if (style === "Cruising (Scenic/Relaxed)") {
    baseMileage *= 1.05; // slightly better mileage
  }

  return {
    mileage: Number(baseMileage.toFixed(1)),
    fuelType: bike.fuelPreference
  };
}

