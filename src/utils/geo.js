/**
 * Extracts fuel prices for a specific location based on Indian state matching
 */
export function getFuelPriceForLocation(locationName, fuelType = 'Normal Petrol') {
  if (!locationName) return STATE_FUEL_PRICES.default.normal;
  const nameLower = locationName.toLowerCase();
  
  let matchedState = 'default';
  for (const state of Object.keys(STATE_FUEL_PRICES)) {
    if (state !== 'default' && nameLower.includes(state)) {
      matchedState = state;
      break;
    }
  }
  
  const prices = STATE_FUEL_PRICES[matchedState];
  return fuelType === 'Power Petrol' || fuelType === 'power' ? prices.power : prices.normal;
}

/**
 * Searches locations in India using Nominatim OpenStreetMap API.
 * Falls back to local database if offline or API error.
 */
export async function searchLocationInIndia(query) {
  if (!query || query.trim().length < 1) return [];

  // Always query local search database first for instant, CORS-free response
  const locals = filterLocalCities(query);
  
  if (!navigator.onLine) {
    return locals;
  }

  try {
    const formattedQuery = encodeURIComponent(query);
    const controller = new AbortController();
    const timeoutId = setTimeout(() => controller.abort(), 5000); // 5s timeout limit for rural/standard connections

    // restrict countrycodes=in ensures results are from India, addressdetails=1 provides state/suburb details
    const response = await fetch(
      `https://nominatim.openstreetmap.org/search?format=json&countrycodes=in&addressdetails=1&q=${formattedQuery}&limit=12`,
      { 
        signal: controller.signal,
        headers: {
          'User-Agent': 'HelpridersBikerPlannerApp/2.0 (contact@helpriders.com)'
        }
      }
    );
    
    clearTimeout(timeoutId);
    
    if (!response.ok) return locals;
    const data = await response.json();
    
    // Combine local results and API results, removing duplicates by name
    const combined = [...locals];
    
    data.forEach(item => {
      let displayName = item.display_name;
      const indiaIndex = displayName.toLowerCase().lastIndexOf(", india");
      const name = indiaIndex !== -1 ? displayName.substring(0, indiaIndex) + ", India" : displayName;
      
      if (!combined.some(c => c.name.toLowerCase() === name.toLowerCase())) {
        combined.push({
          name: name,
          lat: parseFloat(item.lat),
          lon: parseFloat(item.lon),
          state: item.address?.state || item.address?.state_district || ""
        });
      }
    });

    return combined.slice(0, 8);
  } catch (error) {
    console.warn("Geocoding API failed, falling back to local database.", error);
    return locals;
  }
}

